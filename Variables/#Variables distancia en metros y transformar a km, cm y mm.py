#Variables distancia en metros y transformar a km., cm. y mm
metro = int(input("Ingresa la cantidad de metros a convertir: "))
Kilometros = (metro*1) / 1000
Centimetros = metro*100
Milimetros = metro*1000
print(metro, "metros convertidos a kilometros es:", Kilometros, "Kilometros")
print(metro, "metros convertidos a Centimetros es:", Centimetros, "Centimetros")
print(metro, "metros convertidos a Milimetros es:", Milimetros, "Milimetros")